// Complex multiplication: return x*y
func flc_mul(x_re f64, x_im f64, y_re f64, y_im f64) (f64, f64) {
	z_re := f64_sub(f64_mul(x_re, y_re), f64_mul(x_im, y_im))
	z_im := f64_add(f64_mul(x_im, y_re), f64_mul(x_re, y_im))
	return z_re, z_im
}

// Complex multiplication by conjugate: return x*conj(y)
func flc_mulconj(x_re f64, x_im f64, y_re f64, y_im f64) (f64, f64) {
	z_re := f64_add(f64_mul(x_re, y_re), f64_mul(x_im, y_im))
	z_im := f64_sub(f64_mul(x_im, y_re), f64_mul(x_re, y_im))
	return z_re, z_im
}

// Convert a polynomial from normal representation to FFT.
func fpoly_FFT(logn uint, f []f64) {
	n := 1 << logn
	hn := n >> 1
	t := hn
	for lm := uint(1); lm < logn; lm++ {
		m := 1 << lm
		hm := m >> 1
		ht := t >> 1
		j0 := 0
		for i := 0; i < hm; i++ {
			s_re := fgm[((m+i)<<1)+0]
			s_im := fgm[((m+i)<<1)+1]
			for j := 0; j < ht; j++ {
				j1 := j0 + j
				j2 := j1 + ht
				x_re := f[j1]
				x_im := f[j1+hn]
				y_re := f[j2]
				y_im := f[j2+hn]
				z_re, z_im := flc_mul(y_re, y_im, s_re, s_im)
				f[j1] = f64_add(x_re, z_re)
				f[j1+hn] = f64_add(x_im, z_im)
				f[j2] = f64_sub(x_re, z_re)
				f[j2+hn] = f64_sub(x_im, z_im)
			}
			j0 += t
		}
		t = ht
	}
}

// Convert a polynomial from FFT to normal representation.
func fpoly_iFFT(logn uint, f []f64) {
	n := 1 << logn
	hn := n >> 1
	t := 1
	for lm := uint(1); lm < logn; lm++ {
		hm := 1 << (logn - lm)
		dt := t << 1
		j0 := 0
		for i := 0; i < (hm >> 1); i++ {
			s_re := fgm[((hm+i)<<1)+0]
			s_im := fgm[((hm+i)<<1)+1]
			for j := 0; j < t; j++ {
				j1 := j0 + j
				j2 := j1 + t
				x_re := f[j1]
				x_im := f[j1+hn]
				y_re := f[j2]
				y_im := f[j2+hn]
				f[j1] = f64_add(x_re, y_re)
				f[j1+hn] = f64_add(x_im, y_im)
				t_re := f64_sub(x_re, y_re)
				t_im := f64_sub(x_im, y_im)
				z_re, z_im := flc_mulconj(t_re, t_im, s_re, s_im)
				f[j2] = z_re
				f[j2+hn] = z_im
			}
			j0 += dt
		}
		t = dt
	}

	if logn > 1 {
		for i := 0; i < n; i++ {
			f[i] = f64_div2e(f[i], uint32(logn)-1)
		}
	}
}

// Set polynomial d from small polynomial f.
func fpoly_set_small(logn uint, d []f64, f []int8) {
	n := 1 << logn
	for i := 0; i < n; i++ {
		d[i] = f64_of(int64(f[i]))
	}
}

// Add polynomial b to polynomial a; result is written back into a.
func fpoly_add(logn uint, a []f64, b []f64) {
	n := 1 << logn
	for i := 0; i < n; i++ {
		a[i] = f64_add(a[i], b[i])
	}
}

// Subtract polynomial b from polynomial a; result is written back into a.
func fpoly_sub(logn uint, a []f64, b []f64) {
	n := 1 << logn
	for i := 0; i < n; i++ {
		a[i] = f64_sub(a[i], b[i])
	}
}

// Negate polynomial a; result is written back into a.
func fpoly_neg(logn uint, a []f64) {
	n := 1 << logn
	for i := 0; i < n; i++ {
		a[i] = f64_neg(a[i])
	}
}

// Multiply polynomial a by constant x; result is written back into a.
func fpoly_mulconst(logn uint, a []f64, x f64) {
	n := 1 << logn
	for i := 0; i < n; i++ {
		a[i] = f64_mul(a[i], x)
	}
}

// Multiply polynomial a by polynomial b; result is written back into a.
// FFT representation only.
func fpoly_mul_fft(logn uint, a []f64, b []f64) {
	hn := 1 << (logn - 1)
	for i := 0; i < hn; i++ {
		d_re, d_im := flc_mul(a[i], a[i+hn], b[i], b[i+hn])
		a[i] = d_re
		a[i+hn] = d_im
	}
}

// LDL decomposition of a self-adjoint matrix G. The matrix is
// G = [[g00, g01], [adj(g01), g11]], with g00 and g11 being self-adjoint.
// The decomposition is G = L*D*adj(L), with:
//
//	D = [[g00, 0], [0, d11]]
//	L = [[1, 0], [l10, 1]]
//
// g00 is unmodified. Output polynomials l10 and d11 are written over
// g01 and g11, respectively. Only the first n/2 values of g00 and g11
// are read, and only the first n/2 values of d11 are written back into g11.
// All polynomials are in FFT representation.
func fpoly_LDL_fft(logn uint, g00 []f64, g01 []f64, g11 []f64) {
	hn := 1 << (logn - 1)
	for i := 0; i < hn; i++ {
		g00_re := g00[i]
		g01_re := g01[i]
		g01_im := g01[i+hn]
		g11_re := g11[i]
		inv_g00_re := f64_inv(g00_re)
		mu_re := f64_mul(g01_re, inv_g00_re)
		mu_im := f64_mul(g01_im, inv_g00_re)
		zo_re := f64_add(f64_mul(mu_re, g01_re), f64_mul(mu_im, g01_im))
		g11[i] = f64_sub(g11_re, zo_re)
		g01[i] = mu_re
		g01[i+hn] = f64_neg(mu_im)
	}
}

// Split polynomial f into half-size polynomials f0 and f1 such that
// f = f0(x^2) + x*f1(x^2). f0 and f1 are modulo X^(n/2)+1 and have n/2
// coefficients each. All polymials are in FFT representation.
func fpoly_split_fft(logn uint, f0 []f64, f1 []f64, f []f64) {
	hn := 1 << (logn - 1)
	qn := hn >> 1

	if logn == 1 {
		f0[0] = f[0]
		f1[0] = f[hn]
		return
	}

	for i := 0; i < qn; i++ {
		a_re := f[(i<<1)+0]
		a_im := f[(i<<1)+0+hn]
		b_re := f[(i<<1)+1]
		b_im := f[(i<<1)+1+hn]

		t_re := f64_add(a_re, b_re)
		t_im := f64_add(a_im, b_im)
		f0[i] = f64_half(t_re)
		f0[i+qn] = f64_half(t_im)

		t_re = f64_sub(a_re, b_re)
		t_im = f64_sub(a_im, b_im)
		u_re, u_im := flc_mulconj(t_re, t_im,
			fgm[((i+hn)<<1)+0], fgm[((i+hn)<<1)+1])
		f1[i] = f64_half(u_re)
		f1[i+qn] = f64_half(u_im)
	}
}

// Same as fpoly_split_fft() but source f is self-adjoint; only the
// first n/2 coefficients of f are accessed.
func fpoly_split_selfadj_fft(logn uint, f0 []f64, f1 []f64, f []f64) {
	hn := 1 << (logn - 1)
	qn := hn >> 1

	if logn == 1 {
		f0[0] = f[0]
		f1[0] = f64_ZERO
		return
	}

	for i := 0; i < qn; i++ {
		a_re := f[(i<<1)+0]
		b_re := f[(i<<1)+1]

		t_re := f64_add(a_re, b_re)
		f0[i] = f64_half(t_re)
		f0[i+qn] = f64_ZERO

		t_re = f64_half(f64_sub(a_re, b_re))
		f1[i] = f64_mul(t_re, fgm[((i+hn)<<1)+0])
		f1[i+qn] = f64_mul(t_re, f64_neg(fgm[((i+hn)<<1)+1]))
	}
}

// Merge half-size polynomials f0 and f1 into f; this is the opposite
// operation of fpoly_split_fft().
func fpoly_merge_fft(logn uint, f []f64, f0 []f64, f1 []f64) {
	hn := 1 << (logn - 1)
	qn := hn >> 1

	if logn == 1 {
		f[0] = f0[0]
		f[hn] = f1[0]
		return
	}

	for i := 0; i < qn; i++ {
		a_re := f0[i]
		a_im := f0[i+qn]
		b_re := f1[i]
		b_im := f1[i+qn]
		c_re, c_im := flc_mul(b_re, b_im,
			fgm[((i+hn)<<1)+0], fgm[((i+hn)<<1)+1])
		f[(i<<1)+0] = f64_add(a_re, c_re)
		f[(i<<1)+0+hn] = f64_add(a_im, c_im)
		f[(i<<1)+1] = f64_sub(a_re, c_re)
		f[(i<<1)+1+hn] = f64_sub(a_im, c_im)
	}
}

// Given matrix B = [[b00, b01], [b10, b11]], compute the Gram matrix
// G = B*adj(B) = [[g00, g01], [g10, g11]], with:
//
//	g00 = b00*adj(b00) + b01*adj(b01)
//	g01 = b00*adj(b10) + b01*adj(b11)
//	g10 = b10*adj(b00) + b11*adj(b01)
//	g11 = b10*adj(b10) + b11*adj(b11)
//
// Only g00, g01 and g11 are returned, in b00, b01 and b10, respectively.
// b11 is unmodified. All polynomials are in FFT representation.
func fpoly_gram_fft(logn uint, b00 []f64, b01 []f64, b10 []f64, b11 []f64) {
	hn := 1 << (logn - 1)
	for i := 0; i < hn; i++ {
		b00_re := b00[i]
		b00_im := b00[i+hn]
		b01_re := b01[i]
		b01_im := b01[i+hn]
		b10_re := b10[i]
		b10_im := b10[i+hn]
		b11_re := b11[i]
		b11_im := b11[i+hn]

		// g00 = b00*adj(b00) + b01*adj(b01)
		g00_re := f64_add(
			f64_add(f64_sqr(b00_re), f64_sqr(b00_im)),
			f64_add(f64_sqr(b01_re), f64_sqr(b01_im)))
		// g01 = b00*adj(b10) + b01*adj(b11)
		u_re, u_im := flc_mulconj(b00_re, b00_im, b10_re, b10_im)
		v_re, v_im := flc_mulconj(b01_re, b01_im, b11_re, b11_im)
		g01_re := f64_add(u_re, v_re)
		g01_im := f64_add(u_im, v_im)
		// g11 = b10*adj(b10) + b11*adj(b11)
		g11_re := f64_add(
			f64_add(f64_sqr(b10_re), f64_sqr(b10_im)),
			f64_add(f64_sqr(b11_re), f64_sqr(b11_im)))

		b00[i] = g00_re
		b00[i+hn] = f64_ZERO
		b01[i] = g01_re
		b01[i+hn] = g01_im
		b10[i] = g11_re
		b10[i+hn] = f64_ZERO
	}
}

var inv_q = f64mk(6004310871091074, -66)
var minus_inv_q = f64mk(-6004310871091074, -66)

// Given matrix B = [[b00, b01], [b10, b11]], compute the target vector
// [t0,t1] = (1/q)*B*[0,hm], for polynomial hm with coefficients in [0,q-1].
// Only b01 and b11 are needed. hm is in normal representation; all other
// polynomials are in FFT representation.
func fpoly_apply_basis(logn uint,
	t0 []f64, t1 []f64, b01 []f64, b11 []f64, hm []uint16) {

	n := 1 << logn
	for i := 0; i < n; i++ {
		t0[i] = f64_of(int64(hm[i]))
	}
	fpoly_FFT(logn, t0)
	copy(t1[:n], t0[:n])
	fpoly_mul_fft(logn, t1, b01)
	fpoly_mulconst(logn, t1, minus_inv_q)
	fpoly_mul_fft(logn, t0, b11)
	fpoly_mulconst(logn, t0, inv_q)
}

