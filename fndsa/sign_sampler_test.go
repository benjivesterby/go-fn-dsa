func f64str(x f64) string {
	v := f64_to_bits(x)
	return fmt.Sprintf("0x%016X (%v)", v, math.Float64frombits(v))
}

func TestSampler(t *testing.T) {
	// Test vector was generated with the C implementation using a
	// pseudorandom private key in an actual signing operation.
	seed, _ := hex.DecodeString(
		"25a85c14ba612c3bd4339dd01cb2232adca5a78fc7a7d7842c98bea2" +
			"892ce197341957dbb7725bb8d57bc1138db76354da0c148ac3cfdc4a")
	s := newSampler(9, seed)
	for i := 0; i < len(kat_sampler); i++ {
		mu := kat_sampler[i].mu
		isigma := kat_sampler[i].isigma
		out := kat_sampler[i].out
		z := s.next(mu, isigma)
		if z != out {
			t.Fatalf("ERR: i=%d  mu = %s  isigma = %s  -> %d (expected: %d)\n",
				i, f64str(mu), f64str(isigma), z, out)
		}
	}
}

