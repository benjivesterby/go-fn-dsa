func TestSign(t *testing.T) {
	// We use a deterministic pseudorandom source (hardcoded seed).
	var seed [40]byte
	for i := 0; i < len(seed); i++ {
		seed[i] = 0x54
	}
	rng := bytes.NewReader(seed[:])

	// Data is a raw message, not pre-hashed, and context is empty.
	data := []byte("test")

	sig, err := Sign(rng, kat_512_skey, DOMAIN_NONE, 0, data)
	if err != nil {
		t.Fatalf("failure, err = %v", err)
	}
	if !bytes.Equal(sig, kat_512_sig) {
		t.Fatalf("ERR: wrong signature value: %v", sig)
	}
	if !Verify(kat_512_vkey, DOMAIN_NONE, 0, data, sig) {
		t.Fatalf("ERR: signature verification failed")
	}
}

